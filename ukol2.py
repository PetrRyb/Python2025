desetinnéčíslo = int(input("Jaké desetinné číslo chcete zadat?"))
desetinnéčíslo2 = int(input("Jaké druhé desetinné číslo chcete zadat?"))
součet = desetinnéčíslo + desetinnéčíslo2
rozdíl = desetinnéčíslo - desetinnéčíslo2
součin = desetinnéčíslo * desetinnéčíslo2
podíl = desetinnéčíslo / desetinnéčíslo2
print(f"Součet:{součet:.2f}")
print(f"Rozdíl: {rozdíl:.2f}")
print(f"Součin: {součin:.2f}")
print(f"Podíl: {podíl:.2f}")
